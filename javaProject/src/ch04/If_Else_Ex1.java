package ch04;

public class If_Else_Ex1 {

	public static void main(String[] args) {
		//if else문
		int score = 90;// 정수 초기화
		String result = ""; //null 문자로 문자열을 초기화 함
		if(score >= 60) {//>=(이상),<=(이하)
			result="합격";
		
		}else {
			result = "불합격";
		}
		System.out.println(result);
		
		
		
		
		
		
		
		
		
		
		
		
		
	}

}
