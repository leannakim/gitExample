package ch02;

public class TypeEx {

	public static void main(String[] args) {

		int mach;
		int distance;
		mach = 340;
		distance = mach * 60 *60;
		System.out.println("소리가 1시간동안 가는 거리 : " + distance + "m");
		
		
		//실수타입은 double형을 많이 쓴다.
		double radius;
		double area;
		radius = 10.0;//반지름
		area = radius * radius * 3.14;
		System.out.println("반지름이" + radius + "인 원의 넓이 : " + area);
		
		
		
		
		
	}

}
