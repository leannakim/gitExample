package ch03;

public class AndOrEx {

	public static void main(String[] args) {
//논리연산자 &&, ||
		int a = 7;
		System.out.println( a > 5 && a < 0);//true && false => False 논리부정연산자
		System.out.println( a > 5 || a < 0);//true || false => true 
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
	}

}
