package ch03;

public class CslEx2 {
//Printf(함수) 사용하여 출력해 보기
	public static void main(String[] args) {
		
		int a = 20;
		int b = 8;
		System.out.printf("a+b=%d\n",a+b);
		System.out.printf("a-b=%d\n",a-b);
		System.out.printf("a*b=%d\n",a*b);
		System.out.printf("a/b=%d\n",a/b);
		System.out.printf("a%%b=%d\n",a%b);
		
		
		
		
		
	}
	
}
