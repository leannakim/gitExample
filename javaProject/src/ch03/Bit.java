package ch03;

public class Bit {

	public static void main(String[] args) {
//비트연산자( | or, & and, ^ xor)
		System.out.println( 3 | 5);//0011 | 0101 => 0111(십진수:7)
		System.out.println( 3 & 5);//0011 & 0101 => 0001(십진수:1)
		System.out.println( 3 ^ 5);//0011 ^ 0101 => 0110(십진수:6)
		
		
		
		
	}

}
