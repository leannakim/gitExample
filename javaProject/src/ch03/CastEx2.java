package ch03;

public class CastEx2 {

	public static void main(String[] args) {
		
		int a = 500;
		float b = 200.2f;
		int c = 127;
		double d = a;//자동형변환
		
		
		System.out.println("int a => double : " + d);
		
		int e = (int)b;//강제형 변환(명시적 형변환)
		System.out.println("float b => : "+ e);
		
		
		byte f = (byte)c;//강제 형변환 
		System.out.println("byte f => : "+ f);
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
	}

}
