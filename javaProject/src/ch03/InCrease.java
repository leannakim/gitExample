package ch03;

public class InCrease {

	public static void main(String[] args) {
		
		int i = 5;
		int j = 0;
		j = i++; //i의 값을 1 증가 시킴(나중에 계산) -후위연산자
	    System.out.println(i + "," + j);
		i = 5;
		j = ++i;// i의ㅣ 값을 1 증가시킴(먼저계산) -전위 연산자
		System.out.println(i + "," +j);
		
		
		
		
		
		
		
		
		
		
		
		
		
		
	}
}
